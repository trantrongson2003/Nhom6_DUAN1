    private void loadData() {
        DefaultTableModel de = (DefaultTableModel) tblThungRacNV.getModel();
        de.setRowCount(0);
        for (NhanVienViewModel nv : svnv.getListThungRacNV()) {
            de.addRow(new Object[]{
                nv.getMa(), nv.getHoTen(), nv.getTenTK(), nv.getMatKhau(),
                nv.getGioiTinh(), nv.getDiaChi(),
                nv.getSdt(), nv.getTencv(), nv.gettrangthai()
            });
        }
    }

