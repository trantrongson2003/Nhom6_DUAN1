    private void MenuKhoiPhucNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuKhoiPhucNVActionPerformed
        int row = tblThungRacNV.getSelectedRow();

        if (row == - 1) {
            JOptionPane.showMessageDialog(this, "Moi chon dong");
            return;
        }

        int comfirm = JOptionPane.showConfirmDialog(this, "Xác nhận xóa");
        if (comfirm != JOptionPane.YES_OPTION) {
            return;
        }
        String ma = tblThungRacNV.getValueAt(row, 0).toString();

        svnv.KhoiPhucNV(ma);
        loadData();
    }//GEN-LAST:event_MenuKhoiPhucNVActionPerformed

