  private void loadChucVu() {
        cbchucvu.removeAllItems();
        for (ChucVu cv : svcv.getListChucVu()) {
            cbchucvu.addItem(cv.getTen());
        }
    }

    private void loadData() {
        DefaultTableModel de = (DefaultTableModel) tbnhavien.getModel();
        de.setRowCount(0);
        for (NhanVienViewModel nv : svnv.getListNhanVien()) {
            de.addRow(new Object[]{
                nv.getMa(), nv.getHoTen(), nv.getTenTK(), nv.getMatKhau(),
                nv.getGioiTinh(), nv.getDiaChi(),
                nv.getSdt(), nv.getTencv(), nv.gettrangthai()
            });
        }
    }

    public NhanVienViewModel getFrom() {
        NhanVienViewModel nv = new NhanVienViewModel();
        nv.setMa(txtma.getText().trim());
        nv.setHoTen(txtten.getText().trim());
        nv.setTenTK(txttentk.getText().trim());
        nv.setMatKhau(txtmatkhau.getText().trim());
        //nv.setGioiTinh(rdnam.equals("") ? "Nam" : "Nu");
        nv.setGioiTinh(rdnam.isSelected() ? "Nam" : "Nữ");

        nv.setDiaChi(txtdiachi.getText());
        nv.setSdt(txtsdt.getText().trim());
        nv.setTrangThai(cbtrangthai.getSelectedIndex());

        ChucVu cv = svcv.getListChucVu().get(cbchucvu.getSelectedIndex());
        nv.setTencv(cv);

        return nv;
    }
    

