    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        int row = tbnhavien.getSelectedRow();

        if (row == - 1) {
            JOptionPane.showMessageDialog(this, "Moi chon dong");
            return;
        }
        NhanVienViewModel nv = getFrom();

        if (svnv.updateNhanVien(nv.getMa(), nv) != null) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
        }
        loadData();
    }//GEN-LAST:event_btnsuaActionPerformed

    private void byaxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_byaxoaActionPerformed
        // TODO add your handling code here:
        int row = tbnhavien.getSelectedRow();

        if (row == - 1) {
            JOptionPane.showMessageDialog(this, "Moi chon dong");
            return;
        }

        int comfirm = JOptionPane.showConfirmDialog(this, "Xác nhận xóa");
        if (comfirm != JOptionPane.YES_OPTION) {
            return;
        }
        String ma = tbnhavien.getValueAt(row, 0).toString();

        svnv.UpdateThungRacNV(ma);
        loadData();
    }//GEN-LAST:event_byaxoaActionPerformed

